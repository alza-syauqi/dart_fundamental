void main() {
  // variable dengan tipe data
  String name = "Mbappe";
  int age = 23;
  double height = 180.12;
  bool isMarried = false;

  // variable dengan mendeklarasikan var
  var address = "Paris, France";

  print("Name: $name");
  print("Age: $age");
  print("Height: $height");
  print("Is married: $isMarried");
  print("Address: $address");

  // mutable dan immutable variable
  const num1 = 89;
  const int num2 = 76;

  // num1 = 23;
  address = "Semarang, Indonesia";
  print(num1);
  print(num2);
  print(address);
}